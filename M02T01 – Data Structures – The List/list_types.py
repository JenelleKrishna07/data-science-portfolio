# list_types.py
# This script demonstrates list creation, indexing, and printing related data.

# Step 1: Create a list of friends' names
friends_names = ["Sophia", "Liam", "Ava"]

# Step 2: Print the first friend, the last friend, and the length of the list
print(f"First friend: {friends_names[0]}")
print(f"Last friend: {friends_names[-1]}")
print(f"Total number of friends: {len(friends_names)}")

# Step 3: Create a corresponding list of ages
friends_ages = [25, 27, 24]

# Step 4: Print each friend's name and age
print(f"{friends_names[0]} is {friends_ages[0]} years old")
print(f"{friends_names[1]} is {friends_ages[1]} years old")
print(f"{friends_names[2]} is {friends_ages[2]} years old")
