# Task 3: Perform arithmetic with 3 integers

# Get user inputs
num1 = int(input("Enter first integer: "))
num2 = int(input("Enter second integer: "))
num3 = int(input("Enter third integer: "))

# Calculations
print("Sum of all numbers:", num1 + num2 + num3)
print("First minus second:", num1 - num2)
print("Third multiplied by first:", num3 * num1)
print("Sum divided by third:", (num1 + num2 + num3) / num3)
